package com.twojo.model.dto;

public class GFileDTO {
	private long gfilenum;
	private long gpostnum;
	private String gfileorgname;
	private String gfilesysname;
	public long getGfilenum() {
		return gfilenum;
	}
	public void setGfilenum(long gfilenum) {
		this.gfilenum = gfilenum;
	}
	public long getGpostnum() {
		return gpostnum;
	}
	public void setGpostnum(long gpostnum) {
		this.gpostnum = gpostnum;
	}
	public String getGfileorgname() {
		return gfileorgname;
	}
	public void setGfileorgname(String gfileorgname) {
		this.gfileorgname = gfileorgname;
	}
	public String getGfilesysname() {
		return gfilesysname;
	}
	public void setGfilesysname(String gfilesysname) {
		this.gfilesysname = gfilesysname;
	}
	
}
